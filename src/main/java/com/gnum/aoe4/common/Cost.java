package com.gnum.aoe4.common;

public class Cost {

	private int food;
	private int wood;
	private int gold;
	private int stone;
	
	public Cost(int food, int wood, int gold, int stone) {
		this.food = food;
		this.wood = wood;
		this.gold = gold;
		this.stone = stone;
	}
	
	public 

	public int getFood() {
		return food;
	}

	public void setFood(int food) {
		this.food = food;
	}

	public int getWood() {
		return wood;
	}

	public void setWood(int wood) {
		this.wood = wood;
	}

	public int getGold() {
		return gold;
	}

	public void setGold(int gold) {
		this.gold = gold;
	}

	public int getStone() {
		return stone;
	}

	public void setStone(int stone) {
		this.stone = stone;
	}
	
	
	
}
